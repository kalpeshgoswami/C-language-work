#include <stdio.h>

int main()
{
    int a = 10;
    int b = 5;

    printf("lessThan operator %d\n",a<b);

    printf("greaterThan operator %d\n",a>b);

    printf("less or equal operator %d\n",a<=b);

    printf("greater or equal operator %d\n",a>=b);

    printf("equal %d\n",a==b);

    return 0;

}